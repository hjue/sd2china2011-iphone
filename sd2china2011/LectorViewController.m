#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{

    LectorDetailViewController *dvLector = [[LectorDetailViewController alloc]initWithNibName:@"LectorDetailViewController" bundle:nil];
    NSString* _forumID = [NSString stringWithFormat:@"%@", [[forumData objectAtIndex:indexPath.row]objectForKey:@"typeid"]];
    dvLector.forumID = _forumID;
    [self.navigationController pushViewController:dvLector animated:YES];
}

